const REGION_FILTERS: Record<
	Exclude<CountryRegion, 'world'>,
	readonly string[]
> = {
	europe: ['EU'],
	dach: ['DACH'],
	asia: ['AS'],
	africa: ['AF'],
	oceania: ['OC'],
	'north-america': ['NA'],
	'south-america': ['SA'],
	americas: ['NA', 'SA'],
};

const WORLD_COUNTRY_CODES = Array.from(
	new Set(
		Object.values(REGION_COUNTRY_CODES).flatMap((codes) => Array.from(codes)),
	),
).sort();

export type CountryOption = {
	value: string;
	label: string;
};

const getCountryCodes = (region: CountryRegion): readonly string[] => {
	if (region === 'world') {
		return WORLD_COUNTRY_CODES;
	}

	const filterKeys = REGION_FILTERS[region];

	return Array.from(
		new Set(filterKeys.flatMap((key) => REGION_COUNTRY_CODES[key] ?? [])),
	);
};

export const getCountryOptions = (
	region: CountryRegion,
	locale: string,
): CountryOption[] => {
	const displayNames = new Intl.DisplayNames([locale, 'en'], {
		type: 'region',
	});

	return getCountryCodes(region)
		.map((code) => ({ value: code, label: displayNames.of(code) ?? code }))
		.sort((a, b) => a.label.localeCompare(b.label, locale));
};
